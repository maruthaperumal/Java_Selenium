package org.conditional;

public class Practice1 {   // if,else,else if.
	
	public static void main(String[] args) {
		char attendance = 'O';
	
	
	if (attendance == 'P')
	{
		System.out.println("Attendent Classes");
	}
	else if (attendance == 'O')
	{
		System.out.println("On Duty");
	}
	else 
	{
		System.out.println("Not Attendent Classes");
	
	}
	}
}


